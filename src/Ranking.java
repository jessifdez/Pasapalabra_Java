
public class Ranking {
	private String nombre;
	private int aciertos;
	
	public Ranking(String nombre, int aciertos) {
		this.nombre = nombre;
		this.aciertos = aciertos;
	}
	public String getNombre() {
		return nombre;
	}
	public int getAciertos() {
		return aciertos;
	}
	
	

}
